<?php

declare(strict_types=1);

/*
|--------------------------------------------------------------------------
| MYSQLI ERROR REPORTING
|--------------------------------------------------------------------------
*/

mysqli_report(
    MYSQLI_REPORT_ERROR |
    MYSQLI_REPORT_STRICT
);

/*
|--------------------------------------------------------------------------
| DATABASE CONFIGURATION
|--------------------------------------------------------------------------
*/

$host = "localhost";

$user = "root";

$pass = "";

$db = "kvnc";

/*
|--------------------------------------------------------------------------
| DATABASE CONNECTION
|--------------------------------------------------------------------------
*/

try {

    $conn = new mysqli(
        $host,
        $user,
        $pass,
        $db
    );

    /*
    |--------------------------------------------------------------------------
    | CHARACTER SET
    |--------------------------------------------------------------------------
    */

    $conn->set_charset("utf8mb4");

} catch (mysqli_sql_exception $e) {

    http_response_code(500);

    die(
        "Database Connection Failed: " .
        $e->getMessage()
    );
}
?>